""" https://leetcode.com/problems/color-the-triangle-red/
constructive problem, summary pattern by observation
"""
class Solution:
    def colorRed(self, n):
        ans, m = [], n%4

        if m >= 1:
            ans.append([1,1])

        for i in range(2,m+1):
            ans.append([i,1])
            ans.append([i,2*i-1])

        for i in range(m+1,n,4):
            ans.append([i,1])

            for j in range(1,i+1):
                ans.append([i+1,2*j+1])

            ans.append([i+2,2])

            for j in range(i+3):
                ans.append([i+3,2*j+1])

        return ans
    
    
    
